def retorna_dois_valores():
    valor1 = 10
    valor2 = 20
    return valor1, valor2

# Chamando a função e armazenando os valores retornados em duas variáveis
resultado1, resultado2 = retorna_dois_valores()

# Agora, você pode usar resultado1 e resultado2
print(resultado1)  # Saída: 10
print(resultado2)  # Saída: 20
